const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const stored = (() => {
    try { return localStorage.getItem('mineralchain_lang') || 'fr'; } catch (error) { void error; return 'fr'; }
  })();
  const [locale, setLocaleState] = useState(stored);

  const setLocale = useCallback((lang) => {
    setLocaleState(lang);
    try { localStorage.setItem('mineralchain_lang', lang); } catch (error) { void error; }
  }, []);

  // t() — translate key, with optional interpolation vars
  const t = useCallback((key, vars = {}) => {
    const dict = TRANSLATIONS[locale] || TRANSLATIONS.fr;
    let str = dict[key] || TRANSLATIONS.fr[key] || key;
    // Replace {varName} placeholders
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    });
    return str;
  }, [locale]);

  return (
    <I18nContext.Provider value={{ locale, setLocale, lang: locale, setLang: setLocale, t, availableLocales: ['fr', 'en'] }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within I18nProvider');
  return ctx;
}


